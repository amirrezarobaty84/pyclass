number1 = int(input("enter first number \n"))
number2 = int(input("enter second number \n"))
amalgar = input("amalghar khod ra vared konid \n")
if amalgar == "+":
    print(number1 + number2)
elif amalgar == "-":
    print(number1 - number2)
elif amalgar == "/":
    print(number1 / number2)
elif amalgar == "*":
    print(number1 * number2)
else:
    print("man in amalgar ro nemishenasam")

